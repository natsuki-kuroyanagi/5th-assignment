package org.example;

public class Student {

    private int number;

    private int age;

    private String name;

    public int getNumber() {
        return number;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public Student(int number, int age, String name) {
        this.number = number;
        this.age = age;
        this.name = name;


    }
}
